colors = [
    ["Trắng"],
    ["Đen"],
    ["Đỏ"],
    ["Hồng"]
]

sizes = [
    "M",
    "L",
    "XL",
    "XXL",
    "3XL"
]

# Hiển thị danh sách màu
print("Màu:")
for row in colors:
    print("  ".join(row))

# Hiển thị danh sách size
print("\nSize:")
for size in sizes:
    print(size)
